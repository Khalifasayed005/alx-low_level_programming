#include <stdio.h>
/**
 * main - print first 50 Fibonacci numbers, starting with  and
 * Return: Always 0 (success)
 * separated by comma followed by a space
*/

int main(void)
{
	int count;
	unsigned long fib1 = 0, fib2 = 1, sum;

	for (count = 0; count < 50; count++)
	{
		sum = fib1 + fib2;
		printf("%lu", sum);

		fib1 = fib2;
		fib2 = sum;

		if (count == 49)
			printf("\n");
		else
			printf(", ");
	}
	return (0);
}
